#!/usr/bin/env python3
"""Fetch real road geometry for the trip's road legs from the OSRM public
demo server and write it to road-geometry.js.

Runs in CI (GitHub Actions runner has open internet). No API key required.

Output: window.ROAD_GEOMETRY = { "<from>вҶ’<to>": [[lat,lng], ...], ... }
Keys MUST match map.html exactly: `${seg.from}вҶ’${seg.to}` (arrow = U+2192).
A failed segment is simply omitted, so map.html falls back to its waypoints.
"""
import json, sys, time, urllib.request

OSRM = "https://router.project-osrm.org/route/v1/driving/"

# Ordered [lat, lng] waypoints per road leg. Intermediate points steer OSRM
# onto the intended corridor; OSRM snaps everything to the real road network.
