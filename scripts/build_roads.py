def fetch(waypoints):
    coord_str = ";".join(f"{lng},{lat}" for lat, lng in waypoints)
    url = f"{OSRM}{coord_str}?overview=full&geometries=geojson"
    for attempt in range(4):
        try:
            req = urllib.request.Request(url, headers={"User-Agent": "odyssey-trip-roadbuilder"})
            with urllib.request.urlopen(req, timeout=30) as resp:
                data = json.load(resp)
            if data.get("code") != "Ok":
                raise RuntimeError(f"OSRM code={data.get('code')}")
            line = data["routes"][0]["geometry"]["coordinates"]  # [lng, lat] pairs
            return [[round(lat, 5), round(lng, 5)] for lng, lat in line]
        except Exception as e:
            print(f"  attempt {attempt + 1} failed: {e}", file=sys.stderr)
            time.sleep(2 * (attempt + 1))
    return None


def main():
    out = {}
    for key, wps in ROADS.items():
        print(f"routing {key} ...")
        geo = fetch(wps)
        if geo:
            out[key] = geo
            print(f"  ok вҖ” {len(geo)} points")
        else:
            print(f"  FAILED вҖ” map.html will fall back to waypoints")
        time.sleep(1.2)  # be polite to the public demo server

    js = ("// AUTO-GENERATED by scripts/build_roads.py (GitHub Actions). Do not edit by hand.\n"
          "window.ROAD_GEOMETRY = "
          + json.dumps(out, ensure_ascii=False, separators=(",", ":"))
          + ";\n")
    with open("road-geometry.js", "w", encoding="utf-8") as f:
        f.write(js)
    print(f"wrote road-geometry.js with {len(out)}/{len(ROADS)} routes")
    if not out:
        sys.exit("no routes fetched вҖ” refusing to overwrite with empty data")


if __name__ == "__main__":
    main()
